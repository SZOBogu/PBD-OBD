CREATE TABLE SAMOCHODY (
     ID_SAM     NUMBER(4)   CONSTRAINT PK_ID_SAM PRIMARY KEY,
     MARKA      VARCHAR2(12),
     MODEL      VARCHAR2(12),
     NR_REJ     VARCHAR2(8) CONSTRAINT NN_NR_REJ NOT NULL,
     KRAJ_PROD  VARCHAR2(15) ,    
     ROK_PROD   NUMBER(4),
     POJ_SIL NUMBER (4,2)
     
);

CREATE TABLE KLIENCI1 ( 
     ID_KLI      NUMBER(4)   CONSTRAINT PK_ID_KLI PRIMARY KEY,
     NAZWISKO    VARCHAR2(15),
     IMIE        VARCHAR2(15),
     NR_DOWODU   VARCHAR2(9) CONSTRAINT NN_NR_DOWODU NOT NULL,
     MIEJSCOWOSC VARCHAR2(25)
     );

CREATE TABLE  SPECJALNOSC1( 
     ID_SPEC      NUMBER(4)   CONSTRAINT PK_ID_SPEC1 PRIMARY KEY,
     NAZWA    VARCHAR2(15)
); 



CREATE TABLE MAGAZYN ( 
     ID_CZESCI      NUMBER(4)   CONSTRAINT PK_ID_CZESCI PRIMARY KEY,
     NAZWA    VARCHAR2(15), 
     CENA       NUMBER(6,2),
     ILOSC NUMBER (6)
);

CREATE TABLE MECHANIK ( 
     ID_MECH      NUMBER(4)   CONSTRAINT PK_ID_MECH PRIMARY KEY,
     NAZWISKO    VARCHAR2(15),
     IMIE        VARCHAR2(15),
     DATA_ZATRUD DATE,
     ID_SPEC NUMBER (4) CONSTRAINT FK_ID_SPEC REFERENCES SPECJALNOSC1(ID_SPEC)
);




CREATE TABLE NAPRAWA (
     ID_NAP   NUMBER(4) CONSTRAINT PK_ID_NAP PRIMARY KEY,
     ID_SAM   NUMBER(4) CONSTRAINT FK_ID_SAM REFERENCES SAMOCHODY(ID_SAM),
     ID_KLI   NUMBER(4) CONSTRAINT FK_ID_KLI REFERENCES KLIENCI1(ID_KLI),
     ID_MECH  NUMBER(4) CONSTRAINT FK_ID_MECH REFERENCES MECHANIK(ID_MECH),   
     DATA DATE,
     CENA    NUMBER(6,2)
     );


CREATE TABLE CZ_NAPRAW(
     ID_CZNAP NUMBER (4) CONSTRAINT PK_ID_CZNAP PRIMARY KEY,
     ID_NAP   NUMBER(4) CONSTRAINT FK_ID_NAP REFERENCES NAPRAWA(ID_NAP),
     ID_CZESCI   NUMBER(4) CONSTRAINT FK_ID_CZESCI REFERENCES MAGAZYN(ID_CZESCI)
);

